extension TutorialMechanicsCatalog {
    static func mechanics(in category: TutorialMechanicCategory) -> [TutorialMechanic] {
        all.filter { $0.category == category }
    }

    static func mechanics(withWeight weight: DecisionWeight) -> [TutorialMechanic] {
        all.filter { $0.decisionWeight == weight }
    }

    static func mechanic(id: String) -> TutorialMechanic? {
        all.first { $0.id == id }
    }

    static var warmupMechanics: [TutorialMechanic] {
        all.filter(\.appearsInWarmup)
    }

    static var mustTeach: [TutorialMechanic] {
        all.filter { $0.tutorialPriority == .mustTeach }
    }

    static var idsAreUnique: Bool {
        Set(all.map(\.id)).count == all.count
    }
}
